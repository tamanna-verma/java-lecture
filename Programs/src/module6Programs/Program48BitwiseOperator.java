package module6Programs;

public class Program48BitwiseOperator {

	public static void main(String[] args) {
		 
			System.out.println(2&3);//AND OPERATOR
			System.out.println(2|3);//OR OPERATOR
			System.out.println(2^3);//XOR OPERATOR
			System.out.println(~2);//NEGATION OPERATOR
			System.out.println(34<<1);//LEFT SHIFT OPERATOR
			System.out.println(34>>2);//RIGHT SHIFT OPERATOR
			System.out.println(-4<<1);//LEFT SHIFT OPERATOR
			System.out.println(-4>>1);//RIGHT SHIFT OPERATOR
			
			
			

	}

}
